    /**
     * Metodo para habilitar o deshabilitar el formulario de conexion
     *
     * @param bol
     */
    private void enabledForm(boolean bol) {
        txtDriver.setEnabled(bol);
        txtPassword.setEnabled(bol);
        txtUrl.setEnabled(bol);
        txtUser.setEnabled(bol);
        listDatabases.setEnabled(!bol);
        btnGenerate.setEnabled(!bol);
    }

    public void listDataBase() {
        this.listDbs = this.controller.listDatabase();
        DefaultListModel model = new DefaultListModel();
        for (DataBase dataBase : this.listDbs) {
            model.addElement(dataBase);
        }
        listDatabases.setModel(model);
    }
