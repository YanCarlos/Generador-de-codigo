    private void btnConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnectActionPerformed
        if (this.booleanBtn) {
            String driver = txtDriver.getText();
            String url = txtUrl.getText();
            String user = txtUser.getText();
            String pass = new String(txtPassword.getPassword());
            boolean bol = this.controller.establishConnection(driver, url, user, pass);
            this.enabledForm(!bol);
            if (bol) {
                this.listDataBase();
            } else {
                JOptionPane.showMessageDialog(null, "Can't be establish connection");
            }
            this.booleanBtn = false;
            btnConnect.setIcon(this.iconBtnDisconnection);
        }else{
            this.listDbs = new ArrayList<>();
            DefaultListModel model = new DefaultListModel();
            listDatabases.setModel(model);
            this.enabledForm(true);
            btnConnect.setIcon(this.iconBtnConnection);
            this.booleanBtn = true;
        }
    }//GEN-LAST:event_btnConnectActionPerformed

    private void btnGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateActionPerformed
        if (!listDatabases.isSelectionEmpty()) {
            int pos = listDatabases.getSelectedIndex();
            DataBase bd = this.listDbs.get(pos);
            JFileChooser chooser = new JFileChooser();
            chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            int response = chooser.showOpenDialog(this);
            if (response == JFileChooser.APPROVE_OPTION) {
                String path = chooser.getSelectedFile().getAbsolutePath();
                this.controller.generateProject(path, bd);
                JOptionPane.showMessageDialog(null, "The application was created in\n"
                        + path);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Select a database to generate the application");
        }
    }//GEN-LAST:event_btnGenerateActionPerformed

